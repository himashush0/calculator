#include <iostream>
using namespace std;

int main() {
    double x, y;
    char op;

    cout << "Enter first number: ";
    cin >> x;

    cout << "Enter an operator (+, -, *, /): ";
    cin >> op;

    cout << "Enter second number: ";
    cin >> y;

    switch(op) {
        case '+':
            cout << "Result = " << (x + y) << endl;
            break;
        case '-':
            cout << "Result = " << (x - y) << endl;
            break;
        case '*':
            cout << "Result = " << (x * y) << endl;
            break;
        case '/':
            if (y != 0)
                cout << "Result = " << (x / y) << endl;
            else{
                cout << "Error: Division by zero!" << endl;
            }
            break;
        default:
            cout << "Invalid Opreator !!" << endl;
    }

    return 0;
}



